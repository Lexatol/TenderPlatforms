CREATE TABLE IF NOT EXISTS specializations (
    id                      bigserial PRIMARY KEY,
    category_id             bigint references specialization_categories(id),
    title                   varchar(255)
);


CREATE TABLE sub_specializations(
        id          bigserial PRIMARY KEY,
        spec_id     bigint REFERENCES specializations(id),
        title       varchar(500)
);


